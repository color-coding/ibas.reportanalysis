package org.colorcoding.ibas.reportanalysis.reporter;

public class ReporterException extends Exception {

	private static final long serialVersionUID = -4609064523149013915L;

	public ReporterException() {
		super();
	}

	public ReporterException(String arg0) {
		super(arg0);
	}

	public ReporterException(Throwable arg0) {
		super(arg0);
	}

	public ReporterException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
